			            <h2 class="article-title">Profil</h2><br>
			            <p style="text-align: justify; text-justify: inter-word;">
                            Undang-Undang Nomor 14 Tahun 2008 tentang Keterbukaan Informasi Publik (KIP) merupakan pedoman hukum bagi publik untuk mendapatkan hak atas informasi. Undang-Undang KIP tersebut juga menjelaskan kewajiban Badan Publik dalam menyediakan dan melayani permohonan informasi publik secara cepat dan tepat waktu. Selanjutnya dalam Peraturan Pemerintah Republik Indonesia Nomor 61 Tahun 2010 tentang Pelaksanaan UU KIP pada pasal 1 juga mengamanatkan bahwa Badan Publik harus menetapkan Pejabat Pengelola Informasi dan Dokumentasi (PPID) yang bertanggung jawab dalam bidang penyimpanan, pendokumentasian, penyediaan, dan/atau pelayanan informasi di Badan Publik.
                            <br><br>BPAD Pemerintah Provinsi DKI Jakarta sebagai salah satu Badan Publik menyadari bahwa keterbukaan informasi publik merupakan salah satu langkah mewujudkan tata kelola pemerintahan yang baik dan bersih (good governance). Oleh karena itu, untuk  mengimplementasikan  Undang-Undang  Nomor  14   Tahun 2008  tentang Keterbukaan  Informasi Publik dan mendukung pelaksanaan keterbukaan  informasi publik di  tingkat  daerah,  maka  sejak tahun  2013 Pemerintah Provinsi DKI Jakarta telah menerbitkan Peraturan Gubernur Provinsi DKI Jakarta Nomor 48 Tahun 2013 tentang Layanan Informasi Publik. Regulasi tersebut menjadi dasar pengelolaan dan pelayanan informasi publik oleh Pejabat Pengelola Informasi dan Dokumentasi (PPID) di lingkungan Provinsi DKI Jakarta
                        </p>
